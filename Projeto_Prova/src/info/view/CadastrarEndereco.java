    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Sistema si = new Sistema();
        Endereco_1 end = new Endereco_1();
        
        end.setRua(rua.getText());
        end.setNumerocasa(ncasa.getText());
        end.setBairro(bairro.getText());
        end.setLogradouro(log.getText());
        
        try {
            si.CadastrarEndereco(end);
        } catch (Exception ex) {
            Logger.getLogger(CadastrarEndereco.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        JOptionPane.showMessageDialog(null, "Cadastrado com sucesso!");
        
        rua.setText("");
        ncasa.setText("");
        bairro.setText("");
        log.setText("");
    }//GEN-LAST:event_jButton1ActionPerformed

